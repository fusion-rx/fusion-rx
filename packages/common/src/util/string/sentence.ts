export const period = (...vals: any[]) => vals.join(' ') + '.';

export const question = (...vals: any[]) => vals.join(' ') + '?';

export const exclamation = (...vals: any[]) => vals.join(' ') + '!';

export const colon = (...vals: any[]) => vals.join(' ') + ':';

export const semiColon = (...vals: any[]) => vals.join(' ') + ';';

export const comma = (...vals: any[]) => vals.join(' ') + ',';
